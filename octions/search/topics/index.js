const core = require("@actions/core");
const { parse_array, parse_boolean, default_parse } = require('../../../src/utils/parse-input')
const request = require('../../../src/utils/request')

const token = default_parse("token");
const q = default_parse("q");


const previews = [
  "mercy",
]

const inputs = {
  token,
  q,
}


